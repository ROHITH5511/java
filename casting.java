package java_basics;

public class casting {

	public static void main(String[] args) {	
	 
	 int number_1 = 1497865;
	 System.out.println(number_1);                     // assigned an integer // 
	 
	 float number_2 = (float) number_1;
	 System.out.println(number_2);                  // converted integer to float data type //
	 
     double number_3 = (double) number_2;
     System.out.println(number_3);           // converted float too double data type //
	  
	}

}
